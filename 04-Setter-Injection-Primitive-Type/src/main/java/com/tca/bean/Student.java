package com.tca.bean;

public class Student 
{
	private Integer rno;
	private String name;
	
	public void setRno(Integer rno)
	{
		this.rno = rno;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public Integer getRno()
	{
		return rno;
	}
	public String getName()
	{
		return name;
	}
}
